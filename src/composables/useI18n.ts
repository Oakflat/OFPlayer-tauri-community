type Locale = keyof typeof messages
interface MessageTree {
  [key: string]: string | MessageTree
}
type InterpolationParams = Record<string, string | number | boolean | null | undefined>
type I18nWindow = Window & {
  __OFPLAYER_I18N_LISTENER__?: boolean
}

function normalizeLocale(locale: unknown): Locale {
  const value = String(locale ?? '').toLowerCase()

  if (value.startsWith('zh')) {
    return CHINESE_LOCALE
  }

  return DEFAULT_LOCALE
}

function resolveLocale(): Locale {
  if (typeof navigator === 'undefined') {
    return DEFAULT_LOCALE
  }

  const languages =
    Array.isArray(navigator.languages) && navigator.languages.length > 0
      ? navigator.languages
      : [navigator.language]

  return languages.map(normalizeLocale).find((locale) => locale in messages) ?? DEFAULT_LOCALE
}

function resolvePreferredLocale(preference: string = SYSTEM_LANGUAGE): Locale {
  if (preference && preference !== SYSTEM_LANGUAGE) {
    return normalizeLocale(preference)
  }

  return resolveLocale()
}

function applyDocumentLocale(locale: Locale) {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = locale
  }
}

function getMessage(locale: Locale, key: string): unknown {
  return key
    .split('.')
    .reduce<unknown>((value, part) => {
      if (!value || typeof value !== 'object') {
        return undefined
      }

      return (value as MessageTree)[part]
    }, messages[locale] as MessageTree)
}

function interpolate(message: string, params: InterpolationParams) {
  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value))
  }, message)
}

const locale = ref<Locale>(resolveLocale())
const localePreference = ref<string>(SYSTEM_LANGUAGE)

applyDocumentLocale(locale.value)

function syncResolvedLocale() {
  locale.value = resolvePreferredLocale(localePreference.value)
  applyDocumentLocale(locale.value)
}

if (typeof window !== 'undefined') {
  const browserWindow = window as I18nWindow

  if (!browserWindow.__OFPLAYER_I18N_LISTENER__) {
    browserWindow.addEventListener('languagechange', () => {
      if (localePreference.value === SYSTEM_LANGUAGE) {
        syncResolvedLocale()
      }
    })
    browserWindow.__OFPLAYER_I18N_LISTENER__ = true
  }
}

export function setLocalePreference(preference: string = SYSTEM_LANGUAGE) {
  localePreference.value = preference || SYSTEM_LANGUAGE
  syncResolvedLocale()
  return localePreference.value
}

export function getResolvedLocalePreference(preference: string = SYSTEM_LANGUAGE) {
  return resolvePreferredLocale(preference)
}

export function useI18n() {
  function t(key: string, params: InterpolationParams = {}) {
    const message = getMessage(locale.value, key) ?? getMessage(DEFAULT_LOCALE, key)

    if (typeof message !== 'string') {
      return key
    }

    return interpolate(message, params)
  }

  return {
    locale: readonly(locale),
    localePreference: readonly(localePreference),
    t,
  }
}
